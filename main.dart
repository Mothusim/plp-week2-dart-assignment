// Dart program demonstrating different data types

void main() {
  // Integer data type: Used to store whole numbers
  int number = 42;
  print('Integer: $number');

  // Double data type: Used to store decimal numbers
  double doubleNumber = 3.14;
  print('Double: $doubleNumber');

  // String data type: Used to store textual data
  String message = 'Hello, Dart!';
  print('String: $message');

  // List data type: Used to store a collection of elements
  List<int> numbersList = [1, 2, 3, 4, 5];
  print('List: $numbersList');

  // Map data type: Used to store key-value pairs
  Map<String, int> map = {'one': 1, 'two': 2, 'three': 3};
  print('Map: $map');
}
